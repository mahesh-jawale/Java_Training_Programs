package com.programs;

public class DataTypesExample {

	public static void main(String[] args)
	{
		//Byte Data type
		byte byteVar;
		byteVar=-128;
		System.out.println("The minimum value of byte is "+byteVar);
		byteVar=127;
		System.out.println("The maximum value of byte is "+byteVar);
		
		
		//Short Data type
		short shortVar;
		shortVar=-32768;
		System.out.println("The minimum value of short is "+shortVar);
		shortVar=32767;
		System.out.println("The maximum value of short is "+shortVar);		
		
		//int Data type
		int intVar;
		intVar=-2147483648;
		System.out.println("The minimum value of int is "+intVar);
		intVar=2147483647;
		System.out.println("The maximum value of int is "+intVar);	
				
		//Long Data type
		long longVar;
		longVar =-9223372036854775808L;
		System.out.println("The minimum value of long is " + longVar);
		longVar =9223372036854775807L;
		System.out.println("The maximum value of long is " + longVar);
		
		//float Data type
		float floatVar;
		floatVar =2.33f;
		System.out.println("The Value in floatVar is " + floatVar);
		
		//double Data type
		double doubleVar;
		doubleVar =22.8d;
		System.out.println("The Value in doubleVar is " + doubleVar);
		
						
	}

}
